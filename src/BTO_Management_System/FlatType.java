package BTO_Management_System;

/**
 * Enumerates the different types of flats available in BTO projects.
 * Currently, the system supports two-room and three-room flats.
 */
public enum FlatType {
    /**
     * Represents a two-room flat.
     */
    TWOROOM,
    /**
     * Represents a three-room flat.
     */
    THREEROOM
}